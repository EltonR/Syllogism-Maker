package entidade;

public class Predicado {
    
    private int idpredicado;
    private int aridade;
    private String nome;

    public int getIdpredicado() {
        return idpredicado;
    }

    public void setIdpredicado(int idpredicado) {
        this.idpredicado = idpredicado;
    }

    public int getAridade() {
        return aridade;
    }

    public void setAridade(int aridade) {
        this.aridade = aridade;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
}
